// ── Active state ──────────────────────────────────────────────────────────────
var ACTIVE_ROUND_IDX = 0;
var ACTIVE_FLOOR_IDX = 0;

// These are set by switchToRound — treat as read-only elsewhere
var FLOORS = FLOORS_R1;
var PROGRAMS_PER_FLOOR = 7;
var EXIT = { x: 14.5, y: 19.0 };

// Per-floor collected flags — rebuilt by switchToRound
var FLOOR_COLLECTED = [];

function _buildFloorCollected() {
  var rd = ROUNDS[ACTIVE_ROUND_IDX];
  FLOOR_COLLECTED = [];
  for (var f = 0; f < rd.floors.length; f++) {
    var row = [];
    for (var p = 0; p < rd.programsPerFloor; p++) row.push(false);
    FLOOR_COLLECTED.push(row);
  }
}

function switchToRound(roundIdx) {
  ACTIVE_ROUND_IDX = roundIdx;
  var rd = ROUNDS[roundIdx];
  FLOORS            = rd.floors;
  PROGRAMS_PER_FLOOR = rd.programsPerFloor;
  EXIT              = rd.exitPos;
  _buildFloorCollected();
  ACTIVE_FLOOR_IDX  = 0;
  switchToFloor(0);
}

// PROGRAMS is rebuilt by switchToFloor — never reference it directly in map.js
var PROGRAMS = [];

function switchToFloor(idx) {
  ACTIVE_FLOOR_IDX = idx;
  var fl = FLOORS[idx];
  MAP_H = fl.map.length;
  MAP_W = fl.map[0].length;
  PROGRAMS = fl.programs.map(function(p, i) {
    return { x: p.x, y: p.y, label: p.label, collected: FLOOR_COLLECTED[idx][i] };
  });
}

function collectProgram(localIdx) {
  PROGRAMS[localIdx].collected = true;
  FLOOR_COLLECTED[ACTIVE_FLOOR_IDX][localIdx] = true;
}

function totalCollected() {
  var n = 0;
  for (var f = 0; f < FLOOR_COLLECTED.length; f++)
    for (var p = 0; p < FLOOR_COLLECTED[f].length; p++)
      if (FLOOR_COLLECTED[f][p]) n++;
  return n;
}

function totalPrograms() {
  return FLOORS.length * PROGRAMS_PER_FLOOR;
}

function floorCollectedCount(floorIdx) {
  var n = 0;
  for (var p = 0; p < FLOOR_COLLECTED[floorIdx].length; p++)
    if (FLOOR_COLLECTED[floorIdx][p]) n++;
  return n;
}

function mapAt(col, row) {
  if (col < 0 || col >= MAP_W || row < 0 || row >= MAP_H) return 1;
  return FLOORS[ACTIVE_FLOOR_IDX].map[row][col];
}

function isWall(col, row) {
  return mapAt(col, row) !== 0;
}

// GM uses this so his collision runs against his own floor, not the player's.
function isWallOnFloor(col, row, floorIdx) {
  var m = FLOORS[floorIdx].map;
  if (col < 0 || col >= m[0].length || row < 0 || row >= m.length) return true;
  return m[row][col] !== 0;
}

