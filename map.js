// ── String-map parser ─────────────────────────────────────────────────────────
// rows: array of equal-length strings. charMap: { 'X': wallTypeId, ... }.
// Unmapped chars (including ' ' and '.') become walkable floor (0).
function parseMapStrings(rows, charMap) {
  var out = [];
  for (var r = 0; r < rows.length; r++) {
    var line = [];
    for (var c = 0; c < rows[r].length; c++) {
      var ch = rows[r][c];
      line.push(charMap[ch] !== undefined ? charMap[ch] : 0);
    }
    out.push(line);
  }
  return out;
}

